import java.util.*;

public class Sum_Ab {
    public static void main(String[] args) {
        int a, b;
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter Value of A and B:");
        a = sc.nextInt();
        b = sc.nextInt();
        int sum = a + b;
        System.out.println("Sum of A and B : " + sum);
    }
}
